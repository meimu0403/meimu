package lesson1_23;

public class Theme3{

	private String name = "ライオン"; //String nameにライオンを代入　初期値
	public String  getName() {  //nameを取得する
		return this.name;
	}
	public void setName(String name) {  //nameを設定する
		this.name = name;
	}


	private double cm = 2.1; //初期値
	public double  getCm() {   //cmを取得する
		return this.cm;
	}
	public void setCm(double cm) {  //cmを設定する
		this.cm = cm;
	}
	private int km = 80; //初期値
	public int  getKm() {  //kmを取得する
		return this.km;
	}
	public void setKm(int km) {  //kmを設定する
		this.km = km;
	}
}